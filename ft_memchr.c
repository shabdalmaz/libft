#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if ((*(unsigned const char *)s) == (unsigned char)c)
			return ((void *)s);
		s++;
		n--;
	}
	return (NULL);
}
